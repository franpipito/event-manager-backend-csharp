using System;

namespace EventManager
{
	/// <summary>
	/// Description of Empleado.
	/// </summary>
	public class Empleado : Persona
	{
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------		
		//Atributos
		protected int legajo;
		protected double sueldo;
		protected string tarea;
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------		 
		//Constructor
		public Empleado(string nombreYapellido, string documento, int legajo, double sueldo, string tarea) : base(nombreYapellido, documento)
		{
			this.legajo = legajo;
			this.sueldo = sueldo;
			this.tarea = tarea;
		}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------		
		//Propiedades
		public int Legajo
		{
			get{return legajo;}
			set{legajo = value;}
		}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------		
		public double Sueldo
		{
			get{return sueldo;}
			set{sueldo = value;}
		}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------		
		public string Tarea
		{
			get{return tarea;}
			set{tarea = value;}
		}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------		
	}
}
