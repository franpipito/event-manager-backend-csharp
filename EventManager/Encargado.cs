using System;

namespace EventManager
{
	/// <summary>
	/// Description of Encargado.
	/// </summary>
	public class Encargado : Empleado
	{
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------	 	
		//Atributo
		private double plus;
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
		//Constructor
		public Encargado(string nomYape, string dni, int legajo, double sueldo, string tarea, double plus) : base(nomYape, dni, legajo, sueldo, tarea)
		{
			this.plus = plus;
		}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------		
		//Propiedades
		public double Plus
		{
			get{return plus;}
			set{ plus = value;}
		}
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------		
	}
}
